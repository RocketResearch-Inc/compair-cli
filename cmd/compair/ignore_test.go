func TestSuggestCompairIgnoreFindsGeneratedSurfaces(t *testing.T) {
	root := t.TempDir()
	runGit(t, root, "init")
	writeTestFile(t, root, "go.mod", "module example.com/demo\n\ngo 1.24\n")
	writeTestFile(t, root, "go.sum", "example.com/dep v1.0.0 h1:abc\n")
	for _, name := range []string{"a.pb.go", "b.pb.go", "c.pb.go"} {
		writeTestFile(t, root, filepath.Join("proto", name), "// Code generated by protoc. DO NOT EDIT.\npackage proto\n")
	}
	for _, name := range []string{"run.md", "sdk.md"} {
		writeTestFile(t, root, filepath.Join("internal", "studio", "sdk", "docs", "sdks", name), "<!-- This file was automatically generated. -->\n")
	}
	for _, name := range []string{"search.png", "flow.svg", "demo.mp4"} {
		writeTestFile(t, root, filepath.Join("content", "docs", "insights", "assets", name), "media")
	}
	runGit(t, root, "add", ".")

	report, err := suggestCompairIgnore(root, ignoreSuggestOptions{
		MinLargeFileBytes: 250000,
		MinDirFiles:       2,
		MinDirBytes:       1,
		IncludeReview:     true,
	})
	if err != nil {
		t.Fatal(err)
	}
	got := map[string]bool{}
	for _, s := range report.Suggestions {
		got[s.Pattern] = true
	}
	for _, want := range []string{"go.sum", "*.pb.go", "internal/studio/sdk/docs/", "content/docs/insights/assets/"} {
		if !got[want] {
			t.Fatalf("missing suggested pattern %q in %#v", want, report.Suggestions)
		}
	}
}

