func TestWriteCompairIgnoreSuggestionsSkipsReviewByDefault(t *testing.T) {
	root := t.TempDir()
	suggestions := []ignoreSuggestion{
		{Pattern: "go.sum", Confidence: "high", Reason: "generated"},
		{Pattern: "large-fixture.json", Confidence: "review", Reason: "large"},
	}
	written, err := writeCompairIgnoreSuggestions(root, suggestions, false)
	if err != nil {
		t.Fatal(err)
	}
	if len(written) != 1 || written[0] != "go.sum" {
		t.Fatalf("unexpected written patterns: %#v", written)
	}
	content, err := os.ReadFile(filepath.Join(root, ".compairignore"))
	if err != nil {
		t.Fatal(err)
	}
	if string(content) == "" || !strings.Contains(string(content), "go.sum") || strings.Contains(string(content), "large-fixture.json") {
		t.Fatalf("unexpected .compairignore content:\n%s", string(content))
	}
}

func runGit(t *testing.T, root string, args ...string) {
	t.Helper()
	cmd := exec.Command("git", append([]string{"-C", root}, args...)...)
	if out, err := cmd.CombinedOutput(); err != nil {
		t.Fatalf("git %v failed: %v\n%s", args, err, string(out))
	}
}

func writeTestFile(t *testing.T, root, rel, content string) {
	t.Helper()
	path := filepath.Join(root, rel)
	if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
		t.Fatal(err)
	}
	if err := os.WriteFile(path, []byte(content), 0o644); err != nil {
		t.Fatal(err)
	}
}
